import { notFound } from 'next/navigation'
import { getById } from '@/lib/db'
import type { Site } from '@/types'
import ServiceBusinessTemplate from '@/templates/ServiceBusinessTemplate'
import RestaurantTemplate from '@/templates/RestaurantTemplate'
import RetailTemplate from '@/templates/RetailTemplate'
import ProfessionalTemplate from '@/templates/ProfessionalTemplate'
import ModernMinimalTemplate from '@/templates/ModernMinimalTemplate'
import CheckoutButton from '@/components/CheckoutButton'

export const dynamic = 'force-dynamic'

interface PreviewPageProps {
  params: Promise<{
    siteId: string
  }>
}

export default async function PreviewPage({ params }: PreviewPageProps) {
  const { siteId } = await params

  // Fetch site from database
  const { data: site, error } = await getById<Site>('sites', siteId)

  if (error || !site) {
    notFound()
  }

  // Get content data
  const content = site.content_data as any

  if (!content) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="text-center">
          <h1 className="text-2xl font-bold text-gray-900 mb-2">No Content Available</h1>
          <p className="text-gray-600">This site has no content data.</p>
        </div>
      </div>
    )
  }

  // Render the appropriate template based on site.style
  const renderTemplate = () => {
    const props = {
      businessName: content.businessName,
      industry: content.industry,
      heroHeadline: content.heroHeadline,
      heroSubheadline: content.heroSubheadline,
      services: content.services || [],
      about: content.about,
      cta: content.cta,
      colors: content.colors,
      logoUrl: content.logoUrl,
    }

    switch (site.style) {
      case 'service':
        return <ServiceBusinessTemplate {...props} />
      case 'restaurant':
        return <RestaurantTemplate {...props} />
      case 'retail':
        return <RetailTemplate {...props} />
      case 'professional':
        return <ProfessionalTemplate {...props} />
      case 'modern':
      default:
        return <ModernMinimalTemplate {...props} />
    }
  }

  return (
    <div className="relative">
      {renderTemplate()}

      {/* Checkout CTA Section */}
      <section className="bg-gray-900 text-white py-16 text-center">
        <div className="max-w-4xl mx-auto px-4">
          <h2 className="text-3xl font-bold mb-4">
            Ready to Make This Live?
          </h2>
          <p className="text-xl mb-8">
            Just $99/month - Cancel anytime
          </p>
          <CheckoutButton leadId={site.lead_id} />
          <p className="text-sm text-gray-400 mt-4">
            Includes: Domain, Hosting, Email, Unlimited Updates
          </p>
        </div>
      </section>

      {/* Generated by badge */}
      <div className="fixed bottom-4 right-4 bg-black/80 text-white px-4 py-2 rounded-lg shadow-lg text-sm">
        Generated by MVP Web Agency
      </div>
    </div>
  )
}
